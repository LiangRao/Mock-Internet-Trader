 function stockInfo(rawData, name) {
     var myChart = echarts.init(document.getElementById('quoteImg'));
     var dates = rawData.map(function(item) {
         return item[0];
     });

     var data = rawData.map(function(item) {
         return item[4];
     });
     // set html template text and value
     $("#curQuote").text("$" + data[data.length - 1])
     $("#share-price").text("$" + data[data.length - 1])
     $("#sell-share-price").text("$" + data[data.length - 1])
     $("#short-share-price").text("$" + data[data.length - 1])
     $("#cover-share-price").text("$" + data[data.length - 1])
     $("#market-price").val(data[data.length - 1])
     $("input[name='price']").val(data[data.length - 1])

     $(".company").text(name)
     $("input[name='stock_name']").val(name)
     var option = {
         title: {
             text: name
         },
         tooltip: {
             trigger: 'axis'
         },

         grid: {
             left: '3%',
             right: '4%',
             bottom: '3%',
             containLabel: true
         },
         toolbox: {
             feature: {
                 saveAsImage: {}
             }
         },
         xAxis: {
             type: 'category',
             boundaryGap: false,
             data: dates
         },
         yAxis: {
             scale: true,
             type: 'value'
         },
         series: [{
             name: name,
             type: 'line',
             data: data
         }]
     };
     myChart.setOption(option);
 }

