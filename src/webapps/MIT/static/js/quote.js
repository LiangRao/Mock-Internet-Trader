 function isNumberKey(txt, evt) {

     var charCode = (evt.which) ? evt.which : evt.keyCode;
     if (charCode == 46) {
         //Check if the text already contains the . character
         if (txt.value.indexOf('.') === -1) {
             return true;
         } else {
             return false;
         }
     } else {
         if (charCode > 31 &&
             (charCode < 48 || charCode > 57))
             return false;
     }
     return true;
 }


 function getIndexInfo(name) {
     if (name == "") {
         return
     }

     $.get("/get-index-info/" + name)
         .done(function(data) {
             var rawData = $.csv.toArrays(data);
             rawData = rawData.slice(1, )
             stockInfo(rawData, name)
         });
 }

 function getSellInfo(name) {
     var playerid = $("#playerID").val()
     var gameid = $("#gameID").val()
     // validation check
     if (($.isNumeric(playerid) == false) || ($.isNumeric(gameid) == false)) {
         $("#sell-shares").val("0")
         return
     }

     $.ajax({
             method: "GET",
             url: "/get-share-info/" + gameid + "/" + playerid,
             data: { stock_name: $('#search-symbol').data("name"), stock_type: "SELL" }
         })
         .done(function(data) {
             var share = data.share
             var input = $("#sell-shares").val()
             if (input > share) {
                 $("#sell-shares").val(Math.floor(input / 10))
             } else if (input < 0) {
                 $("#sell-shares").val("0")
             }
         });
 }

